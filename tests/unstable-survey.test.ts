/**
 * Be sure that Donobu is installed before running this script...
 *    'npm install donobu' or 'yarn add donobu'
 *
 * Also, be sure that Playwright's browsers are installed...
 *    'npx playwright install'
 */
import { test } from 'donobu';

const testTitle = 'Test for https://unstable-survey-dinoer.replit.app';
const testDetails = {
  annotation: {
    type: 'objective',
    description: `Go through the survey and end the flow.`,
  },
};
test(testTitle, testDetails, async ({ page }) => {
  // Initializing web navigation.
  await page.goto('https://unstable-survey-dinoer.replit.app');
  // Filling out the required field for name input.
  await page.inputText({
    text: 'John Doe',
    finalizeWithSubmit: false,
    selector: {
      element: [
        '#\\:rf\\:-form-item',
        "[placeholder='Enter\\ your\\ name']",
        'div > input:nth-of-type(1)',
        'input',
        'div > :nth-child(2)',
      ],
      frame: null,
    },
  });
  // Providing a contact email address.
  await page.inputRandomizedEmailAddress({
    baseEmail: 'example@gmail.com',
    finalizeWithSubmit: false,
    selector: {
      element: [
        '#\\:rg\\:-form-item',
        "[placeholder='Enter\\ your\\ contact\\ email']",
        'div > input:nth-of-type(1)',
        'input',
        'div > :nth-child(2)',
      ],
      frame: null,
    },
  });
  // Selecting an occupation from the dropdown menu in order to complete the survey.
  await page.clickElement({
    selector: {
      element: [
        '#\\:rh\\:-form-item',
        "[data-state='closed']",
        "button[data-state='closed']",
        "[data-placeholder='']",
        "button[data-placeholder='']",
        "//button[normalize-space(.)='Select your occupation']",
        'div > button:nth-of-type(1)',
        'button',
        'div > :nth-child(2)',
      ],
      frame: null,
    },
  });
  // Choosing an occupation from the list to proceed with the survey.
  await page.clickElement({
    selector: {
      element: [
        "//div[normalize-space(.)='Employed']",
        "div[data-state='unchecked']",
        "[data-radix-collection-item='']",
        "div[data-radix-collection-item='']",
        "[data-state='unchecked']",
        'div > div:nth-of-type(2)',
        'div > :nth-child(2)',
        'div',
      ],
      frame: null,
    },
  });
  // Indicating an interest area to proceed with the survey.
  await page.clickElement({
    selector: {
      element: [
        '#\\:rk\\:-form-item',
        "[data-state='unchecked']",
        "button[data-state='unchecked']",
        'div > button:nth-of-type(1)',
        'button',
        'div > :nth-child(1)',
      ],
      frame: null,
    },
  });
  // Need to scroll to access the 'Next' button.
  await page.scroll({
    direction: 'DOWN',
  });
  // Adding a suggestion to complete optional input field.
  await page.inputText({
    text: 'Looking forward to more surveys.',
    finalizeWithSubmit: false,
    selector: {
      element: [
        '#\\:ro\\:-form-item',
        'div > textarea:nth-of-type(1)',
        'textarea',
        "[placeholder='Share\\ any\\ suggestions']",
        "//label[text()='Suggestions (Optional)']/following-sibling::textarea",
        'div > :nth-child(2)',
      ],
      frame: null,
    },
  });
  // Clicking the 'Next' button to proceed to the next part of the survey.
  await page.clickElement({
    selector: {
      element: [
        "//button[normalize-space(.)='Next']",
        'div > button:nth-of-type(1)',
        'button',
        'div > :nth-child(1)',
      ],
      frame: null,
    },
  });
  // Submitting the survey with the final selections.
  await page.clickElement({
    selector: {
      element: [
        "//button[normalize-space(.)='Submit']",
        'div > button:nth-of-type(2)',
        'button',
        'div > :nth-child(2)',
      ],
      frame: null,
    },
  });
});
